package LoopStatements;
//for loop is that type of condition where initialization, condition and increment/decrement are performed in one go
public class ForLoopEx {

	public static void main(String[] args) {
		
		for(int i=0;i<=4;i++)//i=0 0<=4
		{
			System.out.println(i);//0
			//i++;this operation is always executes here
		}
		
		
		
		

	}

}
